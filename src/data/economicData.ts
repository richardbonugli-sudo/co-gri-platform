/**
 * Calculate global GDP weight for a country
 */
export function calculateGDPWeight(country: string): number {
  const countryGDP = COUNTRY_GDP[country] || 0;
  const totalGDP = Object.values(COUNTRY_GDP).reduce((sum, gdp) => sum + gdp, 0);
  return countryGDP / totalGDP;
}

/**
 * Calculate global trade weight for a country
 */
export function calculateTradeWeight(country: string): number {
  const countryTrade = COUNTRY_TRADE[country] || 0;
  const totalTrade = Object.values(COUNTRY_TRADE).reduce((sum, trade) => sum + trade, 0);
  return countryTrade / totalTrade;
}

/**
 * Calculate country weight (60% GDP + 40% Trade)
 */
export function calculateCountryWeight(country: string): number {
  const gdpWeight = calculateGDPWeight(country);
  const tradeWeight = calculateTradeWeight(country);
  return 0.6 * gdpWeight + 0.4 * tradeWeight;
}

